package corejava.datatypes;

/**
 * Topic: Data Types
 * Program: Data Type Ranges
 * Description: Displays the minimum and maximum values of integer primitive data types.
 * Author: Siddhesh
 */

public class DataTypeRanges {

	public static void main(String[] args) {
		
		System.out.println("==================================");
		System.out.println("         DATA TYPE RANGES         ");
		System.out.println("==================================");
		System.out.println();
		
		System.out.println("Byte");
		System.out.println("Minimum Value : " + Byte.MIN_VALUE);
		System.out.println("Maximum Value : " + Byte.MAX_VALUE);
		System.out.println();
		
		System.out.println("----------------------------------");
		System.out.println();
		
		System.out.println("Short");
		System.out.println("Minimum Value : " + Short.MIN_VALUE);
		System.out.println("Maximum Value : " + Short.MAX_VALUE);
		System.out.println();
		
		System.out.println("----------------------------------");
		System.out.println();
		
		System.out.println("Integer");
		System.out.println("Minimum Value : " + Integer.MIN_VALUE);
		System.out.println("Maximum Value : " + Integer.MAX_VALUE);
		System.out.println();
		
		System.out.println("----------------------------------");
		System.out.println();
		
		System.out.println("Long");
		System.out.println("Minimum Value : " + Long.MIN_VALUE);
		System.out.println("Maximum Value : " + Long.MAX_VALUE);
		System.out.println();
		
		System.out.println("==================================");
	}

}
