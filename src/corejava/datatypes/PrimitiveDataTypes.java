package corejava.datatypes;

/**
 * Topic: Data Types
 * Program: Primitive Data Types
 * Description: Demonstrates all eight primitive data types in Java.
 * Author: Siddhesh
 */

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		
		byte age = 22;
		short year = 2026;
		int salary = 90000;
		long phoneNumber = 9172048532L;
		
		float height = 175.5f;
		double price = 999999.99;
		
		char grade = 'A';
		boolean isEmployee = true;
		
		System.out.println("=================================");
		System.out.println("       PRIMITIVE DATA TYPES      ");
		System.out.println("=================================");
		System.out.println();
		
		System.out.println("Byte Value      : " + age);
		System.out.println("Short Value     : " + year);
		System.out.println("Integer Value   : " + salary);
		System.out.println("Long Value      : " + phoneNumber);
		System.out.println("Float Value     : " + height);
		System.out.println("Double Value    : " + price);
		System.out.println("Character Value : " + grade);
		System.out.println("Boolean Value   : " + isEmployee);
		System.out.println();
		
		System.out.println("=================================");	
	}

}
