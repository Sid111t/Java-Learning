package corejava.datatypes;

/**
 * Topic: Data Types
 * Program: Type Selection Examples
 * Description: Demonstrates selecting appropriate data types for different kinds of data.
 * Author: Siddhesh
 */

public class TypeSelectionExamples {

	public static void main(String[] args) {
		
		byte studentAge = 18;
		short admissionYear = 2026;
		int salary = 25000;
		long mobileNumber = 9172048532L;
		
		float height = 170.5f;
		double productPrice = 4999.99;
		
		char grade = 'A';
		boolean isPlaced = false;
		String studentName = "Rahul";
		
		System.out.println("=================================");
		System.out.println("     TYPE SELECTION EXAMPLES     ");
		System.out.println("=================================");
		System.out.println();
		
		System.out.println("Student Age    : " + studentAge);
		System.out.println("Admission Year : " + admissionYear);
		System.out.println("Salary         : " + salary);
		System.out.println("Mobile Number  : " + mobileNumber);
		System.out.println("Height         : " + height);
		System.out.println("Product Price  : " + productPrice);
		System.out.println("Grade          : " + grade);
		System.out.println("Is Placed      : " + isPlaced);
		System.out.println("Student Name   : " + studentName);
		System.out.println();
		
		System.out.println("=================================");
		
	}

}
