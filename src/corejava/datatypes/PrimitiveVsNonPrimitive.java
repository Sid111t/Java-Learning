package corejava.datatypes;

/**
 * Topic: Data Types
 * Program: Primitive vs Non-Primitive
 * Description: Demonstrates the difference between primitive and non-primitive data types.
 * Author: Siddhesh
 */

public class PrimitiveVsNonPrimitive {

	public static void main(String[] args) {
		
		int age = 40;
		double salary = 80000.00;
		char grade = 'A';
		boolean isEmployee = true;
		String name = "Rohit";
		String city = "Mumbai";
		
		System.out.println("=================================");
		System.out.println("    PRIMITIVE VS NON-PRIMITIVE   ");
		System.out.println("=================================");
		System.out.println();
		
		System.out.println("Primitive Data Types");
		System.out.println();
		
		System.out.println("Age         : " + age);
		System.out.println("Salary      : " + salary);
		System.out.println("Grade       : " + grade);
		System.out.println("Is Employee : " + isEmployee);
		System.out.println();
		
		System.out.println("---------------------------------");
		System.out.println();
		
		System.out.println("Non-Primitive Data Types");
		System.out.println();
		
		System.out.println("Name        : " + name);
		System.out.println("City        : " + city);
		System.out.println();
		
		System.out.println("=================================");
	}

}
