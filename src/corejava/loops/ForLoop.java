package corejava.loops;

/**
 * Topic: Loops
 * Program: Print Numbers Using For Loop
 * Description: Demonstrates the use of a for loop to print numbers from 1 to 10.
 * Author: Siddhesh
 */

public class ForLoop {

	public static void main(String[] args) {
		
		// Display the results
		
		System.out.println("==================================");
		System.out.println("        PRINT NUMBERS (FOR)       ");
		System.out.println("==================================");
		System.out.println();
		
		System.out.println("Numbers from 1 to 10");
		System.out.println();
		
		System.out.println("----------------------------------");
		System.out.println();

		// Print the numbers using a for loop
		
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		System.out.println();
		System.out.println("==================================");

	}

}
