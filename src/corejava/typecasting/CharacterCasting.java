package corejava.typecasting;

/**
 * Topic: Type Casting
 * Program: Character Casting
 * Description: Demonstrates conversion between char and int data types.
 * Author: Siddhesh
 */

public class CharacterCasting {

	public static void main(String[] args) {
		
		// Declare and initialize variables
		
		char grade = 'A';
		
		// Perform character type casting
		
		int ascii = grade;
		
		// Display the results
		
		System.out.println("==================================");
		System.out.println("         CHARACTER CASTING        ");
		System.out.println("==================================");
		System.out.println();
		
		System.out.println("Original Character Value : " + grade);
		System.out.println("Converted Integer Value  : " + ascii);
		System.out.println();
		
		System.out.println("==================================");

	}

}
